package de.lubowiecki.gui.todos;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.stage.Stage;

import java.io.IOException;

public class App extends Application {
    @Override
    public void start(Stage stage) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(App.class.getResource("todo-view.fxml"));
        Scene scene = new Scene(fxmlLoader.load());
        // CSS in die Szene einbinden
        scene.getStylesheets().add(getClass().getResource("/styles/basic.css").toExternalForm());
        stage.setTitle("Hello!");
        stage.setScene(scene);
        stage.show();
    }

    public static void main(String[] args) {
        launch();
    }
}